          <h1>HRA Calculation</h1>
          <hr style="margin-top:10px;margin-bottom: 10px">
      
          <!-- Preview Image -->
          <!--<img class="img-fluid rounded" src="http://placehold.it/900x300" alt="">-->
          <!-- Post Content -->
          <p>The House Rent Allowance (HRA) is an essential component of an individual's salary that defines the total amount allotted by the employer towards the employee's accommodation as rent. The HRA amount can be beneficial for an employee as it is calculated for tax deductions for a particular financial year. The HRA helps in reducing the taxable income that you are liable to pay. The HRA tax benefits are only applicable for those employees who stay in a rental accommodation. If an individual is staying in his/her own house, he/she won't be eligible to claim the amount for tax deductions. The calculation of HRA is based on various factors, such as the entitlement to 50% of the basic salary, if the employee is staying in a metro city (40% for other cities). The calculation of HRA for tax benefit is considered from one of the following three listed provisions:</p><br>

          <ul>
            <li>The actual amount allotted by the employer as the HRA.</li>

            <li>Actual rent paid less 10% of the basic salary.</li>

            <li>50% of the basic salary, if the employee is staying in a metro city (40% for a non-metro city).</li>
          </ul>
          

