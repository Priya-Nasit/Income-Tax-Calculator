          <p>Income tax refers to the tax levied by the government for the purpose of financing its various operations. There are two types of taxes, direct and indirect. While Income Tax is a direct tax, VAT, service tax, excise and the latest one to subsume all these taxes Goods and Services Tax (GST) are all indirect taxes. Apart from funding the activities of the government, taxes also act as a fiscal stabiliser that aid in distributing wealth evenly among the population. Furthermore, taxes are instrumental in cushioning the effects of economic cycles. The payment of Income Tax in India is made according to the provisions made under the Income Tax Act.</p><br>

          <p>The government imposes a tax on taxable income of all persons who are individuals, Hindu Undivided Families (HUF's), companies, firms, LLP, association of persons, body of individuals, local authority and any other artificial juridical person. Levy of tax on a person depends upon his residential status. The CBDT administers the Income Tax Department, which is a part of the Department of Revenue under the Ministry of Finance, Govt. of India. Income tax is a key source of funds that the government uses to fund its activities and serve the public.</p>
          

