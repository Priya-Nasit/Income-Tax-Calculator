          <!-- Post Content -->
          <p>The deductions on Income from Salary falls under the Section 16 of the Income Tax Act. The deductions are:</p><br>

          <ul>
            <li>Entertainment Allowance under Section 16(ii): Deduction is allowed by way of entertainment allowance given by an employer. This deduction is available only for the Government employees. The deduction is either the 1/5th of salary without including the benefits or perquisites or other allowances or Rs.5,000, whichever is lesser. The non-government employees can't avail this deduction.</li><br>

            <li>Tax on Employment under Section 16(iii): The Professional Tax is allowed as a deduction while computing income from salaries.</li>
          </ul>
          

